		<p class="text-md text-justify font-poppins md:text-xl">
			The answers lie in the dark patterns of design—intentional tactics created not to help you but
			to trick, coerce, or pressure you into decisions you wouldn’t normally make.
		</p>

		<p class="text-md text-justify font-poppins md:text-xl">
			Take, for example, the “urgency triggers” on shopping sites like Flipkart or Myntra. Those
			bright red warnings about limited stock? Often, they’re just marketing ploys designed to
			create a false sense of scarcity. Or consider how streaming platforms like Hotstar auto-enroll
			you into paid subscriptions after a free trial—by pre-ticking the renewal box. Suddenly,
			you’re paying for something you didn’t even know you’d agreed to.
		</p>


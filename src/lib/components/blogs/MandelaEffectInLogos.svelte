		<p class="text-justify font-poppins text-md md:text-xl">
			One theory suggests that confabulation might be responsible for these false memories.
			Confabulation occurs when our brains fill in gaps in our memory with made-up, distorted, or
			misunderstood information. For instance, we might mix up similar visuals or combine elements
			from different sources, creating a new but incorrect memory. The power of suggestion and
			social reinforcement can make these inaccuracies stronger. When many people share the same
			false memory, it becomes widely accepted as true, further reinforcing the incorrect
			perception.
		</p>

		<p class="text-justify font-poppins text-md md:text-xl">
